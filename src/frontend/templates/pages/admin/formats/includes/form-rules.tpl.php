<ul class="fd-list --spaced">
  <li>
    <strong>Name</strong>:
    <ul class="fd-list">
      <li>
        Must be <strong>unique</strong> among formats
      </li>
      <li>
        Single-cluster format names must include " Cluster". Ex.: "Grimm Cluster"
      </li>
    </ul>
  </li>
  <li>
    <strong>Code</strong>:
    <ul class="fd-list">
      <li>
        Must be exactly <strong>5</strong> characters long
      </li>
      <li>
        Must be <strong>unique</strong> among formats
      </li>
      <li>
        Single-cluster format codes must follow the pattern <kbd>c-xxx</kbd> where <kbd>xxx</kbd> is a 3-letter code representing the cluster. Ex.: "c-gri"
      </li>
    </ul>
  </li>
  <li>
    <strong>Default</strong>: There's only <strong>one</strong> default format per time. Setting a format as default removes the "default" flag on all others.
  </li>
</ul>
